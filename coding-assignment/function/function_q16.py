# 16. Question: Write a function to find the intersection of two sets.
#     Input: Two sets 'set1' and 'set2'.
#     Output: A set containing the intersection of the two sets.
#     Explanation: The function returns the set of elements that are present in both sets.
def intersection_of_sets(set1, set2):
    """Returns the intersection of two sets."""
    return set1 & set2
