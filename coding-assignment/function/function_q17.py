# 17. Question: Write a function to find the union of two sets.
#     Input: Two sets 'set1' and 'set2'.
#     Output: A set containing all the elements from both sets (union).
#     Explanation: The function combines all elements from both sets, eliminating duplicates.
def union_of_sets(set1, set2):
    """Returns the union of two sets."""
    return set1 | set2
