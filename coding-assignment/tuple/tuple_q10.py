# 10. Write a Python program to check whether an element exists within a tuple

# Sample Output

# ('T', 'u', 't', 'o', 'r', ' ', 'J', 'o', 'e', 's',8)

# 8 in Tuple = True

# 'x' in Tuple = False

t = ('T', 'u', 't', 'o', 'r', ' ', 'J', 'o', 'e', 's',8)
print('t' in t)
print(8 in t)
print('x' in t)
print(2 in t)

